/**
 * Playwright Config Template Generator
 *
 * Generates Playwright configuration files (TypeScript or JavaScript)
 * using template-based approach (no LLM calls).
 */

export interface PlaywrightConfigOptions {
  testDir: string;       // Relative path from config file to test directory
  baseURL?: string;      // Optional base URL for tests
  typescript: boolean;   // Output format (TypeScript or JavaScript)
}

/**
 * Generate Playwright configuration file content
 *
 * @param options Configuration options
 * @returns Configuration file content as string
 */
export function generatePlaywrightConfig(options: PlaywrightConfigOptions): string {
  const { testDir, baseURL, typescript } = options;

  if (typescript) {
    return generateTypeScriptConfig(testDir, baseURL);
  } else {
    return generateJavaScriptConfig(testDir, baseURL);
  }
}

